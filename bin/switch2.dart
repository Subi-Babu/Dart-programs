void main(){
   var month = "september";
   switch(month){
     case "january" : print('month is january');
     break;
     case "february" : print('month is february');
     break;
     case "april" : print('month is april');
     break;
     case 'march' : print('month is march');
     break;
     case 'november' : print('month is november');
     break;
     case 'september' : print('month is september');
     break;
     default : print('none of these');

   }
}
